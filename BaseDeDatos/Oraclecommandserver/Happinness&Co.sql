/* CREACION DE LAS TABLAS */
CREATE TABLE USUARIOS( 
    ID number(10),
    NOMBRE varchar2(100) NOT NULL,
    EMAIL varchar2(100) NOT NULL UNIQUE,
    PASSWORD varchar2(255) NOT NULL,
    CONSTRAINT pk_USUARIOS PRIMARY KEY (ID)
);

CREATE TABLE EVENTOS( 
    ID number(10),
    FECHA date NOT NULL,
    TITULO varchar2(200) NOT NULL,   
    UBICACION varchar2(255) NOT NULL,
    DESCRIPCION varchar2(1000) NOT NULL, 
    CONSTRAINT pk_EVENTOS PRIMARY KEY (ID)
);

CREATE TABLE GALERIAS( 
    ID number(10),
    TITULO varchar2(200) NOT NULL,
    ID_EVENTO number(10) NOT NULL,
    CONSTRAINT pk_GALERIAS PRIMARY KEY (ID),
    CONSTRAINT fk_GALERIAS FOREIGN KEY (ID_EVENTO) REFERENCES EVENTOS(ID) ON DELETE CASCADE
);

CREATE TABLE IMAGENES_GALERIAS( 
    ID number(10),
    TITULO varchar2(200) NOT NULL,
    RUTA_IMAGEN varchar2(500) NOT NULL, 
    ID_GALERIA number(10) NOT NULL,
    CONSTRAINT pk_IMAGENES_GALERIAS PRIMARY KEY (ID),
    CONSTRAINT fk_IMAGENES_GALERIAS FOREIGN KEY (ID_GALERIA) REFERENCES GALERIAS(ID) ON DELETE CASCADE
);

CREATE TABLE FAVORITOS( 
    ID_USUARIO number(10) NOT NULL,
    ID_EVENTO number(10) NOT NULL,
    CONSTRAINT pk_FAVORITOS PRIMARY KEY (ID_USUARIO, ID_EVENTO),
    CONSTRAINT fk_FAV_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES USUARIOS(ID) ON DELETE CASCADE,
    CONSTRAINT fk_FAV_EVENTO FOREIGN KEY (ID_EVENTO) REFERENCES EVENTOS(ID) ON DELETE CASCADE
);


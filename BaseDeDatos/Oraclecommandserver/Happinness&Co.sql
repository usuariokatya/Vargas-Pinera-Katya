/* A. Galerias anteriores al 28-02-2026 */
CREATE OR REPLACE VIEW VISTA_GALERIAS_ANTERIORES AS
SELECT GALERIAS.TITULO
FROM GALERIAS, EVENTOS
WHERE (GALERIAS.ID_EVENTO = EVENTOS.ID) 
AND (EVENTOS.FECHA < TO_DATE('28/02/2026', 'DD/MM/YYYY'));

/* B. Eventos favoritos del usuario 1 */
CREATE OR REPLACE VIEW VISTA_FAVORITOS_USUARIO1 AS
SELECT EVENTOS.TITULO, EVENTOS.FECHA, EVENTOS.UBICACION
FROM EVENTOS, FAVORITOS
WHERE (EVENTOS.ID = FAVORITOS.ID_EVENTO) 
AND (FAVORITOS.ID_USUARIO = 1);

/* C. Imagenes de la galeria del evento con ID 2 */
CREATE OR REPLACE VIEW VISTA_IMAGENES_EVENTO2 AS
SELECT IMAGENES_GALERIAS.TITULO, IMAGENES_GALERIAS.RUTA_IMAGEN
FROM IMAGENES_GALERIAS, GALERIAS
WHERE (IMAGENES_GALERIAS.ID_GALERIA = GALERIAS.ID) 
AND (GALERIAS.ID_EVENTO = 2);

/* D. Favoritos del usuario 2 posteriores al 28-02-2026 */
CREATE OR REPLACE VIEW VISTA_FAVORITOS_USUARIO2_PROXIMOS AS
SELECT EVENTOS.TITULO, EVENTOS.FECHA
FROM EVENTOS, FAVORITOS
WHERE (EVENTOS.ID = FAVORITOS.ID_EVENTO) 
AND (FAVORITOS.ID_USUARIO = 2) 
AND (EVENTOS.FECHA > TO_DATE('28/02/2026', 'DD/MM/YYYY'));